class AlertManager:
    """Manages alert rules and notifications"""
    
    def __init__(self, metrics_collector: MetricsCollector):
        self.collector = metrics_collector
        self.rules = []
        self.active_alerts = {}
        self.notification_callbacks = []
    
    def add_rule(self, metric_name: str, condition: str, threshold: float, 
                 severity: AlertSeverity, message_template: str):
        """Add an alert rule"""
        rule = {
            'metric_name': metric_name,
            'condition': condition,  # 'gt', 'lt', 'eq', 'gte', 'lte'
            'threshold': threshold,
            'severity': severity,
            'message_template': message_template
        }
        self.rules.append(rule)
        logger.info(f"Added alert rule: {metric_name} {condition} {threshold}")
    
    def evaluate_rules(self):
        """Evaluate all rules against recent metrics"""
        for rule in self.rules:
            metric_name = rule['metric_name']
            recent_metrics = self.collector.get_recent_metrics(metric_name, minutes=5)  # Last 5 minutes
            
            if not recent_metrics:
                continue
            
            latest_value = recent_metrics[-1].value
            
            # Check condition
            condition_met = False
            if rule['condition'] == 'gt' and latest_value > rule['threshold']:
                condition_met = True
            elif rule['condition'] == 'lt' and latest_value < rule['threshold']:
                condition_met = True
            elif rule['condition'] == 'gte' and latest_value >= rule['threshold']:
                condition_met = True
            elif rule['condition'] == 'lte' and latest_value <= rule['threshold']:
                condition_met = True
            elif rule['condition'] == 'eq' and latest_value == rule['threshold']:
                condition_met = True
            
            if condition_met:
                # Check if we already have an active alert for this rule
                alert_key = f"{metric_name}_{rule['condition']}_{rule['threshold']}"
                if alert_key not in self.active_alerts:
                    # Create and trigger alert
                    message = rule['message_template'].format(
                        value=latest_value,
                        threshold=rule['threshold'],
                        metric=metric_name
                    )
                    
                    self.collector.trigger_alert(
                        metric_name=metric_name,
                        severity=rule['severity'],
                        message=message,
                        value=latest_value,
                        threshold=rule['threshold']
                    )
                    
                    # Track active alert
                    self.active_alerts[alert_key] = datetime.utcnow()
                else:
                    # Update the timestamp of the active alert
                    self.active_alerts[alert_key] = datetime.utcnow()
            else:
                # Clear the alert if condition is no longer met
                alert_key = f"{metric_name}_{rule['condition']}_{rule['threshold']}"
                if alert_key in self.active_alerts:
                    del self.active_alerts[alert_key]
    
    def add_notification_callback(self, callback: Callable[[Alert], None]):
        """Add a notification callback"""
        self.notification_callbacks.append(callback)
        self.collector.register_alert_callback(callback)


class MonitoringService:
    """Main monitoring service that orchestrates all components"""
    
    def __init__(self):
        self.collector = MetricsCollector(retention_hours=24)
        self.analytics = AnalyticsEngine(self.collector)
        self.dashboard = DashboardGenerator(self.analytics)
        self.alert_manager = AlertManager(self.collector)
        self.running = False
        self.monitoring_thread = None
        
        # Add default alert rules
        self._setup_default_rules()
    
    def _setup_default_rules(self):
        """Setup default monitoring rules"""
        # CPU usage > 80% - Warning
        self.alert_manager.add_rule(
            metric_name='system_cpu_percent',
            condition='gt',
            threshold=80.0,
            severity=AlertSeverity.WARNING,
            message_template='High CPU usage detected: {value:.2f}% (threshold: {threshold}%)'
        )
        
        # Memory usage > 90% - Critical
        self.alert_manager.add_rule(
            metric_name='system_memory_percent',
            condition='gt',
            threshold=90.0,
            severity=AlertSeverity.CRITICAL,
            message_template='Critical memory usage: {value:.2f}% (threshold: {threshold}%)'
        )
        
        # Low battery < 20% - Warning
        self.alert_manager.add_rule(
            metric_name='telemetry_battery_level',
            condition='lt',
            threshold=20.0,
            severity=AlertSeverity.WARNING,
            message_template='Low battery level: {value:.2f}% (threshold: {threshold}%)'
        )
    
    def start(self):
        """Start the monitoring service"""
        self.running = True
        self.monitoring_thread = threading.Thread(target=self._monitoring_loop, daemon=True)
        self.monitoring_thread.start()
        logger.info("Monitoring service started")
    
    def stop(self):
        """Stop the monitoring service"""
        self.running = False
        if self.monitoring_thread:
            self.monitoring_thread.join(timeout=5)
        logger.info("Monitoring service stopped")
    
    def _monitoring_loop(self):
        """Main monitoring loop"""
        while self.running:
            try:
                # Collect system metrics
                system_metrics = self.collector.get_system_metrics()
                for metric_name, value in system_metrics.items():
                    self.collector.collect_metric(
                        name=f"system_{metric_name}",
                        value=value,
                        source="system_monitor",
                        metric_type=MetricType.SYSTEM
                    )
                
                # Evaluate alert rules
                self.alert_manager.evaluate_rules()
                
                # Sleep for 30 seconds before next iteration
                time.sleep(30)
                
            except Exception as e:
                logger.error(f"Monitoring loop error: {e}")
                time.sleep(5)  # Brief pause before retrying
    
    def collect_custom_metric(self, name: str, value: float, source: str = "custom",
                             metric_type: MetricType = MetricType.CUSTOM,
                             unit: Optional[str] = None,
                             tags: Optional[Dict[str, str]] = None):
        """Collect a custom metric"""
        self.collector.collect_metric(name, value, source, metric_type, unit, tags)
    
    def get_analytics_report(self, metric_name: str) -> Dict[str, Any]:
        """Get comprehensive analytics for a metric"""
        summary = self.collector.get_metrics_summary(metric_name)
        trend = self.analytics.analyze_trends(metric_name)
        anomalies = self.analytics.detect_anomalies(metric_name)
        predictions = self.analytics.predict_future_values(metric_name, periods=5)
        
        return {
            'summary': summary,
            'trend': trend,
            'anomalies_count': len(anomalies),
            'predictions': predictions,
            'last_updated': datetime.utcnow().isoformat()
        }


