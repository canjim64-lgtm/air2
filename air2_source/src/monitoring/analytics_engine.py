class MetricType(Enum):
    """Types of metrics that can be monitored"""
    PERFORMANCE = "performance"
    SECURITY = "security"
    SYSTEM = "system"
    APPLICATION = "application"
    CUSTOM = "custom"


class AlertSeverity(Enum):
    """Severity levels for alerts"""
    INFO = "info"
    WARNING = "warning"
    CRITICAL = "critical"
    EMERGENCY = "emergency"


@dataclass
class Metric:
    """Represents a single metric measurement"""
    name: str
    value: float
    timestamp: datetime
    source: str
    metric_type: MetricType
    tags: Optional[Dict[str, str]] = None
    unit: Optional[str] = None


@dataclass
class Alert:
    """Represents an alert generated by the system"""
    id: str
    metric_name: str
    severity: AlertSeverity
    message: str
    timestamp: datetime
    value: float
    threshold: float
    tags: Optional[Dict[str, str]] = None


class MetricsCollector:
    """Collects and stores metrics from various system components"""
    
    def __init__(self, retention_hours: int = 24):
        self.retention_period = timedelta(hours=retention_hours)
        self.metrics_buffer = deque(maxlen=10000)  # Circular buffer for recent metrics
        self.alerts_buffer = deque(maxlen=1000)    # Circular buffer for recent alerts
        self.metric_store = defaultdict(list)      # Store metrics by name
        self.alert_callbacks = []
        self.lock = threading.Lock()
        
        # Initialize database for persistent storage
        self.db_conn = sqlite3.connect("monitoring.db", check_same_thread=False)
        self._init_database()
        
        logger.info(f"Metrics collector initialized with {retention_hours} hour retention")
    
    def _init_database(self):
        """Initialize the monitoring database"""
        cursor = self.db_conn.cursor()
        
        # Create metrics table
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS metrics (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                name TEXT NOT NULL,
                value REAL NOT NULL,
                timestamp TEXT NOT NULL,
                source TEXT NOT NULL,
                metric_type TEXT NOT NULL,
                tags TEXT,
                unit TEXT
            )
        """)
        
        # Create alerts table
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS alerts (
                id TEXT PRIMARY KEY,
                metric_name TEXT NOT NULL,
                severity TEXT NOT NULL,
                message TEXT NOT NULL,
                timestamp TEXT NOT NULL,
                value REAL NOT NULL,
                threshold REAL NOT NULL,
                tags TEXT
            )
        """)
        
        # Create indexes for performance
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_metrics_timestamp ON metrics(timestamp)")
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_metrics_name ON metrics(name)")
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_alerts_timestamp ON alerts(timestamp)")
        
        self.db_conn.commit()
        logger.info("Monitoring database initialized")
    
    def collect_metric(self, name: str, value: float, source: str, 
                      metric_type: MetricType, unit: Optional[str] = None,
                      tags: Optional[Dict[str, str]] = None):
        """Collect a metric measurement"""
        metric = Metric(
            name=name,
            value=value,
            timestamp=datetime.utcnow(),
            source=source,
            metric_type=metric_type,
            tags=tags,
            unit=unit
        )
        
        with self.lock:
            # Add to in-memory buffer
            self.metrics_buffer.append(metric)
            
            # Add to named store
            self.metric_store[name].append(metric)
            
            # Store in database
            cursor = self.db_conn.cursor()
            cursor.execute("""
                INSERT INTO metrics (name, value, timestamp, source, metric_type, tags, unit)
                VALUES (?, ?, ?, ?, ?, ?, ?)
            """, (
                metric.name, metric.value, metric.timestamp.isoformat(),
                metric.source, metric.metric_type.value,
                json.dumps(metric.tags) if metric.tags else None,
                metric.unit
            ))
            self.db_conn.commit()
        
        logger.debug(f"Collected metric: {name} = {value} ({unit or 'no unit'})")
    
    def register_alert_callback(self, callback: Callable[[Alert], None]):
        """Register a callback for alert notifications"""
        self.alert_callbacks.append(callback)
    
    def trigger_alert(self, metric_name: str, severity: AlertSeverity, 
                     message: str, value: float, threshold: float,
                     tags: Optional[Dict[str, str]] = None):
        """Trigger an alert"""
        alert = Alert(
            id=secrets.token_urlsafe(16),
            metric_name=metric_name,
            severity=severity,
            message=message,
            timestamp=datetime.utcnow(),
            value=value,
            threshold=threshold,
            tags=tags
        )
        
        with self.lock:
            # Add to in-memory buffer
            self.alerts_buffer.append(alert)
            
            # Store in database
            cursor = self.db_conn.cursor()
            cursor.execute("""
                INSERT INTO alerts (id, metric_name, severity, message, timestamp, value, threshold, tags)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?)
            """, (
                alert.id, alert.metric_name, alert.severity.value, alert.message,
                alert.timestamp.isoformat(), alert.value, alert.threshold,
                json.dumps(alert.tags) if alert.tags else None
            ))
            self.db_conn.commit()
        
        # Call registered callbacks
        for callback in self.alert_callbacks:
            try:
                callback(alert)
            except Exception as e:
                logger.error(f"Alert callback failed: {e}")
        
        logger.warning(f"Alert triggered: {severity.value.upper()} - {message} (value: {value}, threshold: {threshold})")
    
    def get_recent_metrics(self, name: str, minutes: int = 60) -> List[Metric]:
        """Get recent metrics for a specific name"""
        cutoff_time = datetime.utcnow() - timedelta(minutes=minutes)
        
        with self.lock:
            return [m for m in self.metric_store[name] if m.timestamp >= cutoff_time]
    
    def get_metrics_summary(self, name: str, minutes: int = 60) -> Dict[str, Any]:
        """Get statistical summary of metrics"""
        metrics = self.get_recent_metrics(name, minutes)
        if not metrics:
            return {}
        
        values = [m.value for m in metrics]
        timestamps = [m.timestamp for m in metrics]
        
        return {
            'count': len(values),
            'mean': statistics.mean(values),
            'median': statistics.median(values),
            'std_dev': statistics.stdev(values) if len(values) > 1 else 0,
            'min': min(values),
            'max': max(values),
            'first_timestamp': min(timestamps),
            'last_timestamp': max(timestamps),
            'trend': 'increasing' if len(values) > 1 and values[-1] > values[0] else 'decreasing'
        }
    
    def get_alerts(self, hours: int = 24, severity: Optional[AlertSeverity] = None) -> List[Alert]:
        """Get recent alerts"""
        cutoff_time = datetime.utcnow() - timedelta(hours=hours)
        
        with self.lock:
            alerts = [a for a in self.alerts_buffer if a.timestamp >= cutoff_time]
        
        if severity:
            alerts = [a for a in alerts if a.severity == severity]
        
        return alerts
    
    def get_system_metrics(self) -> Dict[str, float]:
        """Get system-level metrics"""
        cpu_percent = psutil.cpu_percent(interval=1)
        memory_percent = psutil.virtual_memory().percent
        disk_percent = psutil.disk_usage('/').percent if hasattr(psutil, 'disk_usage') else 0
        
        # Get GPU metrics if available
        gpu_percent = 0
        gpu_memory_percent = 0
        try:
            gpus = GPUtil.getGPUs()
            if gpus:
                gpu = gpus[0]  # Use first GPU
                gpu_percent = gpu.load * 100
                gpu_memory_percent = gpu.memoryUtil * 100
        except Exception as e:
            # GPU not available or GPUtil not installed
            self.logger.debug(f"GPU monitoring unavailable: {e}")

        return {
            'cpu_percent': cpu_percent,
            'memory_percent': memory_percent,
            'disk_percent': disk_percent,
            'gpu_percent': gpu_percent,
            'gpu_memory_percent': gpu_memory_percent,
            'process_count': len(psutil.pids()),
            'network_io_mb': psutil.net_io_counters().bytes_sent / (1024*1024) + psutil.net_io_counters().bytes_recv / (1024*1024)
        }


class AnalyticsEngine:
    """Performs advanced analytics on collected metrics"""
    
    def __init__(self, metrics_collector: MetricsCollector):
        self.collector = metrics_collector
        self.anomaly_detectors = {}
        self.trend_analyzers = {}
        self.correlation_analyzers = {}
    
    def detect_anomalies(self, metric_name: str, method: str = 'zscore', threshold: float = 2.0) -> List[Metric]:
        """Detect anomalies in a metric using various methods"""
        metrics = self.collector.get_recent_metrics(metric_name, minutes=120)  # Last 2 hours
        if len(metrics) < 10:  # Need sufficient data points
            return []
        
        values = np.array([m.value for m in metrics])
        
        if method == 'zscore':
            z_scores = np.abs(stats.zscore(values))
            anomaly_indices = np.where(z_scores > threshold)[0]
        elif method == 'iqr':
            Q1 = np.percentile(values, 25)
            Q3 = np.percentile(values, 75)
            IQR = Q3 - Q1
            lower_bound = Q1 - 1.5 * IQR
            upper_bound = Q3 + 1.5 * IQR
            anomaly_indices = np.where((values < lower_bound) | (values > upper_bound))[0]
        else:
            raise ValueError(f"Unknown anomaly detection method: {method}")
        
        anomalies = [metrics[i] for i in anomaly_indices]
        return anomalies
    
    def analyze_trends(self, metric_name: str, window_minutes: int = 60) -> Dict[str, Any]:
        """Analyze trends in a metric over time"""
        metrics = self.collector.get_recent_metrics(metric_name, minutes=window_minutes)
        if len(metrics) < 2:
            return {'trend': 'insufficient_data', 'slope': 0, 'confidence': 0}
        
        values = [m.value for m in metrics]
        timestamps = [(m.timestamp - metrics[0].timestamp).total_seconds() for m in metrics]
        
        # Calculate linear regression
        slope, intercept, r_value, p_value, std_err = stats.linregress(timestamps, values)
        
        trend_direction = 'increasing' if slope > 0 else 'decreasing' if slope < 0 else 'stable'
        
        return {
            'trend': trend_direction,
            'slope': slope,
            'confidence': r_value ** 2,  # R-squared value
            'p_value': p_value,
            'std_error': std_err,
            'data_points': len(values)
        }
    
    def correlate_metrics(self, metric1: str, metric2: str, minutes: int = 60) -> Dict[str, Any]:
        """Calculate correlation between two metrics"""
        metrics1 = self.collector.get_recent_metrics(metric1, minutes)
        metrics2 = self.collector.get_recent_metrics(metric2, minutes)
        
        # Align timestamps
        df1 = pd.DataFrame([(m.timestamp, m.value) for m in metrics1], columns=['timestamp', 'value1'])
        df2 = pd.DataFrame([(m.timestamp, m.value) for m in metrics2], columns=['timestamp', 'value2'])
        
        # Merge on timestamp (approximate alignment)
        merged = pd.merge_asof(df1.sort_values('timestamp'), df2.sort_values('timestamp'), 
                              on='timestamp', tolerance=pd.Timedelta(seconds=30), direction='nearest')
        
        if len(merged) < 2:
            return {'correlation': 0, 'p_value': 1, 'sample_size': len(merged)}
        
        correlation, p_value = stats.pearsonr(merged['value1'], merged['value2'])
        
        return {
            'correlation': correlation,
            'p_value': p_value,
            'sample_size': len(merged),
            'significant': p_value < 0.05
        }
    
    def predict_future_values(self, metric_name: str, periods: int = 10) -> List[float]:
        """Simple prediction using linear extrapolation"""
        metrics = self.collector.get_recent_metrics(metric_name, minutes=60)
        if len(metrics) < 5:
            return [m.value for m in metrics[-periods:]] if metrics else []
        
        values = [m.value for m in metrics]
        timestamps = [(m.timestamp - metrics[0].timestamp).total_seconds() for m in metrics]
        
        # Calculate linear trend
        slope, intercept = np.polyfit(timestamps, values, 1)
        
        # Predict future values
        last_timestamp = timestamps[-1]
        future_values = []
        for i in range(1, periods + 1):
            future_time = last_timestamp + (i * 60)  # Assume 1-minute intervals
            predicted_value = slope * future_time + intercept
            future_values.append(predicted_value)
        
        return future_values


